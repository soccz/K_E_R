#!/bin/bash
# K_E_R 결과물 GitHub push.
# - run_weekly.sh가 자동 호출
# - 수동 실행도 가능: bin/push_results.sh
#
# 푸시 대상:
#   - companies/   (보고서·섹션·프롬프트 스냅샷·메타)
#   - industry_notes/
#   - _index.md, _glossary.md (있으면)
#
# 푸시 안 하는 것:
#   - .env, *.log, *.pdf, xbrl/* (모두 .gitignore)

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$REPO_ROOT"

# git repo가 아니면 skip (init 안 됨)
if ! git rev-parse --is-inside-work-tree &>/dev/null; then
  echo "[push] git repo 아님 — skip (git init + remote 설정 필요)"
  exit 0
fi

# 변경 대상 stage
TARGETS=()
for d in companies industry_notes; do
  [ -d "$d" ] && TARGETS+=("$d")
done
for f in _index.md _glossary.md _watchlist.md; do
  [ -f "$f" ] && TARGETS+=("$f")
done

if [ ${#TARGETS[@]} -eq 0 ]; then
  echo "[push] stage할 대상 없음 — skip"
  exit 0
fi

# 보안 스캐너 — secret 들어간 파일 commit 차단
if [ -x "$REPO_ROOT/.venv/bin/python" ]; then
  echo "[push] secret 스캐너 검사..."
  if ! "$REPO_ROOT/.venv/bin/python" -m pipeline.security_check "${TARGETS[@]}"; then
    echo "[push] secret 패턴 발견 — push 차단. 수동 검토 필요."
    exit 1
  fi
fi

git add "${TARGETS[@]}"

# 변경 없으면 skip
if git diff --cached --quiet; then
  echo "[push] stage된 변경 없음 — skip"
  exit 0
fi

# Commit
TS=$(date '+%Y-%m-%d %H:%M %Z')
COMMIT_MSG="auto: report update $TS

Generated by K_E_R systemd timer."
git commit -m "$COMMIT_MSG"

# Push (실패해도 cron job은 fail 처리 안 함 — 다음 실행 때 재시도)
if git push origin main 2>&1; then
  echo "[push] 성공"
else
  echo "[push] 실패 — 다음 실행 때 재시도 (네트워크/auth 점검)"
  exit 0  # 의도적 0 — push 실패가 cron job 실패로 이어지지 않게
fi
